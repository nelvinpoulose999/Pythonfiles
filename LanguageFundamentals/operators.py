# ARITHMETIC operators +,-,/,*,//,**
print(10+2)# add
print(10-2)# subtract
print(10/3) # division
print(10*3)# multiply
print(10//3) # floor division
print(10**3) # power
print(10%3)  # modulus


#LOGICAL OPERATORS
# | - OR, & - AND, ^ - XOR

print(2&4) #0 - 0000 in binary
print(2|4) #6 - 0110 in binary
print(2^4) #6 - 0110 in binary